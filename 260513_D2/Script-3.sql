CREATE TABLE USERS (
    ID          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USERNAME    VARCHAR2(255) NOT NULL,
    EMAIL       VARCHAR2(255) NOT NULL UNIQUE,
    PASSWORD    VARCHAR2(255) NOT NULL, -- 비밀번호 컬럼 추가
    USER_ROLE   VARCHAR2(20) DEFAULT 'USER' CHECK (USER_ROLE IN ('USER', 'ADMIN')),
    EXPERIENCE  VARCHAR2(20) CHECK (EXPERIENCE IN ('beginner', 'intermediate', 'advanced')),
    ASSET       VARCHAR2(20) CHECK (ASSET IN ('stock', 'etf', 'bonds', 'real_estate')),
    GOAL        VARCHAR2(20) CHECK (GOAL IN ('short_term', 'mid_term', 'income')),
    RISK        VARCHAR2(20) CHECK (RISK IN ('conservative', 'neutral', 'aggressive')),
    KNOWLEDGE   VARCHAR2(20) CHECK (KNOWLEDGE IN ('basic', 'intermediate', 'advanced')),
    CREATED_AT  TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE etf_meta (
