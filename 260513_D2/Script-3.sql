-- ETF 일별 시세 정보를 저장하는 테이블
CREATE TABLE ETF_PRICE (
    BAS_DT      VARCHAR2(8),   -- 기준일자 (YYYYMMDD)
    ISIN_CD     VARCHAR2(20),  -- 표준코드 (ISIN)
    ITMS_NM     VARCHAR2(100), -- 종목명
    CLPR        NUMBER,        -- 종가
    VS          NUMBER,        -- 전일대비 등락
    FLUC_RT     NUMBER,        -- 등락률
    MKP         NUMBER,        -- 시가
    HIPR        NUMBER,        -- 고가
    LOPR        NUMBER,        -- 저가
    TR_QUANT    NUMBER,        -- 거래량
    TR_PRC      NUMBER         -- 거래대금
);

-- 데이터 조회 성능 향상을 위한 인덱스 설정
CREATE INDEX IDX_ETF_PRICE_BAS_DT ON ETF_PRICE(BAS_DT);
CREATE INDEX IDX_ETF_PRICE_ISIN_CD ON ETF_PRICE(ISIN_CD);



CREATE TABLE RECOMMENDATIONS (
    ID          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID     NUMBER NOT NULL,
    ISIN        VARCHAR2(255) NOT NULL,
    SCORE       NUMBER(5), -- 추천 점수
    CREATED_AT  TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    -- 외래 키 설정 (USERS 테이블의 ID 참조)
    CONSTRAINT FK_REC_USER_ID FOREIGN KEY (USER_ID) REFERENCES USERS(ID) ON DELETE CASCADE
);

-- 외래 키 조인을 위한 인덱스 생성 (성능 최적화)
CREATE INDEX IDX_REC_USER_ID ON RECOMMENDATIONS(USER_ID);

